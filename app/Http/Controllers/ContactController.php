     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $update = Contact::find($id); 

        $update->map = $request->map; 
        $update->titre = $request->titre; 
        $update->phone = $request->phone;  
        
        $update->save(); 

        return redirect()->back(); 
    }

